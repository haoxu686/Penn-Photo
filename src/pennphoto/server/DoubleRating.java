package pennphoto.server;

public class DoubleRating {

	private float scoreX;
	private float scoreY;
	public float getScoreX() {
		return scoreX;
	}
	public void setScoreX(float scoreX) {
		this.scoreX = scoreX;
	}
	public float getScoreY() {
		return scoreY;
	}
	public void setScoreY(float scoreY) {
		this.scoreY = scoreY;
	}
	
}
